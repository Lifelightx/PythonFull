class Student:
    def __init(self):
        self.name = ""
        self.age = 0
        self.grade = ""
    def showDetails(self):
        print("Name : ", self.name)
        print("Age : ", self.age)
        print("Grade : ", self.grade)


s1 = Student()
s1.name = "Jeeban"
s1.age = 12
s1.grade = "A"

s1.showDetails()


